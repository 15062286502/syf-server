package com.example.syfserver.entity;

public class VxUserEntity {
    private String id;
    private String vxName;
    private String vxImg;
    private String vxAddress;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getVxName() {
        return vxName;
    }

    public void setVxName(String vxName) {
        this.vxName = vxName;
    }

    public String getVxImg() {
        return vxImg;
    }

    public void setVxImg(String vxImg) {
        this.vxImg = vxImg;
    }

    public String getVxAddress() {
        return vxAddress;
    }

    public void setVxAddress(String vxAddress) {
        this.vxAddress = vxAddress;
    }


}
